// Builds Knowledge.md — a single-file digest of every fact this repo
// tracks (timeline, islands, crews, characters, devil fruits, ships,
// swords, organizations, glossary), pulled straight from the curated
// data files with zero new prose invented here. It exists so a reader
// (human or a sub-agent briefed on this project) has one place to look
// instead of piecing the story together across a dozen source files.
//
// Regenerate any time the underlying data changes:
//   node scripts/build-knowledge-base.mjs
import { writeFileSync } from 'fs';

import { STRAWHATS } from '../src/data/characters/strawhats.js';
import { MARINES } from '../src/data/characters/admirals-and-marines.js';
import { WARLORDS_AND_EMPERORS } from '../src/data/characters/warlords-and-emperors.js';
import { WORST_GENERATION } from '../src/data/characters/worst-generation.js';
import { REVOLUTIONARIES } from '../src/data/characters/revolutionaries.js';
import { GIANTS_AND_MINKS } from '../src/data/characters/giants-and-minks.js';
import { SUPPORTING_CAST } from '../src/data/characters/supporting-cast.js';
import { MINOR_CAST_STUBS } from '../src/data/characters/minor-cast-stubs.js';
import { MINOR_CAST_STUBS_2 } from '../src/data/characters/minor-cast-stubs-2.js';
import { MINOR_CAST_STUBS_3 } from '../src/data/characters/minor-cast-stubs-3.js';
import { LEGENDARY_FIGURES } from '../src/data/characters/legendary-figures.js';
import { MINOR_CAST_STUBS_4 } from '../src/data/characters/minor-cast-stubs-4.js';
import { LOCATIONS, SAGAS } from '../src/data/locations.js';
import { CREWS } from '../src/data/crews.js';
import { DEVIL_FRUITS } from '../src/data/devilFruits.js';
import { SHIPS } from '../src/data/ships.js';
import { ORGANIZATIONS } from '../src/data/organizations.js';
import { SWORDS } from '../src/data/swords.js';
import { GLOSSARY } from '../src/data/glossary.js';
import { TIMELINE } from '../src/data/timeline.js';

let generatedCount = 0;
try {
  generatedCount = ((await import('../src/data/characters/generated-full-roster.js')).GENERATED_ROSTER || []).length;
} catch { /* not generated yet */ }

const TIERS = [
  ['Straw Hat Pirates', STRAWHATS],
  ['Marines', MARINES],
  ['Warlords & Emperors', WARLORDS_AND_EMPERORS],
  ['Worst Generation', WORST_GENERATION],
  ['Revolutionary Army', REVOLUTIONARIES],
  ['Giants & Minks', GIANTS_AND_MINKS],
  ['Supporting Cast', SUPPORTING_CAST],
  ['Legendary & Mythic Figures', LEGENDARY_FIGURES],
];
const curatedIds = new Set(TIERS.flatMap(([, list]) => list).map(c => c.id));
// Name lookups (devil fruit/sword/org holders, `related` links) should
// resolve against every curated character, including stub-tier minor
// cast — not just the tiers that get their own dossier section below.
const STUB_TIERS = [...MINOR_CAST_STUBS, ...MINOR_CAST_STUBS_2, ...MINOR_CAST_STUBS_3, ...MINOR_CAST_STUBS_4];
const charById = new Map([...TIERS.flatMap(([, list]) => list), ...STUB_TIERS].map(c => [c.id, c]));
const locById = new Map(LOCATIONS.map(l => [l.id, l]));
const crewById = new Map(CREWS.map(c => [c.id, c]));

const lines = [];
const p = (s = '') => lines.push(s);

p('# One Piece Grand Line Atlas — Knowledge Base');
p();
p('Fan-made, unofficial reference. Auto-generated by `scripts/build-knowledge-base.mjs` ' +
  'from this repo\'s own structured data files — nothing here is copied from any wiki or ' +
  'official source, and nothing is invented beyond what those data files already record. ' +
  'Regenerate after editing any `src/data/*.js` file so this stays in sync.');
p();
p('Intended as a single-file briefing document — for a newcomer to the story, or as context ' +
  'handed to a sub-agent that needs the whole picture without reading a dozen source files.');
p();

// --- Timeline ---
p('## Story Timeline');
p();
p('| Saga | Chapters | Published | Key Locations |');
p('|---|---|---|---|');
TIMELINE.forEach(t => {
  const keyLocs = (t.keyLocations || []).map(id => locById.get(id)?.name || id).join(', ');
  p(`| ${t.saga} | ${t.chapterRange} | ${t.yearsPublished} | ${keyLocs} |`);
});
p();

// --- World Atlas, grouped by saga in story order ---
p('## World Atlas');
p();
SAGAS.forEach(saga => {
  const locs = LOCATIONS.filter(l => l.saga === saga);
  if (!locs.length) return;
  p(`### ${saga}`);
  p();
  locs.forEach(loc => {
    p(`**${loc.name}** — ${loc.sea} · _${loc.status}_`);
    p();
    p(loc.summary);
    p();
    if (loc.problem) p(`**Problem:** ${loc.problem}`);
    if (loc.resolution) p(`**Resolution:** ${loc.resolution}`);
    if (loc.outcome) p(`**Outcome:** ${loc.outcome}`);
    if (loc.problem || loc.resolution || loc.outcome) p();
    if (loc.events?.length){
      p('What happened here:');
      loc.events.forEach(e => p(`- ${e}`));
      p();
    }
    if (loc.foreshadowing) { p(`**Foreshadowing:** ${loc.foreshadowing}`); p(); }
    if (loc.characters?.length){
      p(`Characters tied to this island: ${loc.characters.map(c => `${c.name} (${c.role})`).join(', ')}`);
      p();
    }
  });
});

// --- Crews & Organizations ---
p('## Crews');
p();
CREWS.forEach(crew => {
  const statusNote = crew.status === 'disbanded' ? ` — disbanded (${crew.disbandedContext || 'reason unrecorded'})` : ` — ${crew.status}`;
  p(`### ${crew.name}${statusNote}`);
  if (crew.notes) p(`_${crew.notes}_`);
  p();
  crew.members.forEach(m => {
    const name = charById.get(m.id)?.name || m.id;
    const note = m.note ? ` — ${m.note}` : '';
    p(`- ${name}: ${m.role} (${m.status})${note}`);
  });
  p();
});

p('## Organizations');
p();
ORGANIZATIONS.forEach(org => {
  p(`### ${org.name} — ${org.type} (${org.status})`);
  p(org.note);
  if (org.leader) p(`Leader: ${charById.get(org.leader)?.name || org.leader}`);
  p();
});

// --- Devil Fruits ---
p('## Devil Fruits');
p();
p('| Fruit | Type | Effect | Current User | Status |');
p('|---|---|---|---|---|');
DEVIL_FRUITS.forEach(f => {
  const user = f.currentUser ? (charById.get(f.currentUser)?.name || f.currentUser) : '—';
  p(`| ${f.name} | ${f.type} | ${f.effect} | ${user} | ${f.status} |`);
});
p();

// --- Ships ---
p('## Ships');
p();
SHIPS.forEach(s => {
  const crewName = s.crew ? (crewById.get(s.crew)?.name || s.crew) : 'unaffiliated';
  p(`- **${s.name}** (${crewName}, ${s.status}) — ${s.note}`);
});
p();

// --- Swords ---
p('## Named Swords');
p();
SWORDS.forEach(sw => {
  const owner = sw.currentOwner ? (charById.get(sw.currentOwner)?.name || sw.currentOwner) : 'unclaimed';
  p(`- **${sw.name}** (${sw.grade}), currently held by ${owner} — ${sw.note}`);
});
p();

// --- Character dossiers ---
p('## Characters');
p();
TIERS.forEach(([tierName, list]) => {
  p(`### ${tierName}`);
  p();
  list.forEach(c => {
    const epithet = c.epithet ? ` "${c.epithet}"` : '';
    p(`#### ${c.name}${epithet}`);
    p(`${c.role || ''}${c.affiliation ? ` · ${c.affiliation}` : ''} · status: ${c.status}`);
    if (c.summary) p(c.summary);
    if (c.bounty) p(`Bounty: ${c.bounty}`);
    if (c.devilFruit) p(`Devil Fruit: ${c.devilFruit.name} (${c.devilFruit.type}) — ${c.devilFruit.effect}`);
    if (c.dream) p(`Dream: ${c.dream}`);
    if (c.weapon) p(`Fighting style: ${c.weapon}`);
    if (c.haki?.length) p(`Haki: ${c.haki.join(', ')}`);
    if (c.related?.length){
      p(`Related: ${c.related.map(r => `${charById.get(r.id)?.name || r.id} (${r.type})`).join('; ')}`);
    }
    p();
  });
});

p(`_Plus ${generatedCount} additional named characters tracked at stub level (name/affiliation/role ` +
  `only, no summary yet) — see \`src/data/characters/generated-full-roster.js\`._`);
p();

// --- Glossary ---
p('## Glossary');
p();
GLOSSARY.forEach(g => p(`- **${g.term}** — ${g.definition}`));
p();

writeFileSync('Knowledge.md', lines.join('\n'));
console.log(`Wrote Knowledge.md (${lines.length} lines)`);
