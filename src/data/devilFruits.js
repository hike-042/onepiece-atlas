export function findFruitByUser(characterId){
  return DEVIL_FRUITS.find(f => f.currentUser === characterId) || null;
}

export function fruitHistory(fruitId){
  const fruit = DEVIL_FRUITS.find(f => f.id === fruitId);
  if (!fruit) return [];
  return [...fruit.formerUsers, fruit.currentUser].filter(Boolean);
}
